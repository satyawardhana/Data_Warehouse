<?php

namespace app\controllers;

use Yii;
use yii\filters\AccessControl;
use yii\web\Controller;
use yii\web\Response;
use yii\filters\VerbFilter;
use app\models\LoginForm;
use app\models\ContactForm;

class SiteController extends Controller
{
    /**
     * {@inheritdoc}
     */
    public function behaviors()
    {
        return [
            'access' => [
                'class' => AccessControl::className(),
                'only' => ['logout'],
                'rules' => [
                    [
                        'actions' => ['logout'],
                        'allow' => true,
                        'roles' => ['@'],
                    ],
                ],
            ],
            'verbs' => [
                'class' => VerbFilter::className(),
                'actions' => [
                    'logout' => ['post'],
                ],
            ],
        ];
    }

    /**
     * {@inheritdoc}
     */
    public function actions()
    {
        return [
            'error' => [
                'class' => 'yii\web\ErrorAction',
            ],
            'captcha' => [
                'class' => 'yii\captcha\CaptchaAction',
                'fixedVerifyCode' => YII_ENV_TEST ? 'testme' : null,
            ],
        ];
    }

    /**
     * Displays homepage.
     *
     * @return string
     */
    public function actionIndex()
    {
        $DB = Yii::$app->db;
        $branch = $DB->createCommand("select branch.branch_name, SUM(fact_sales.units_sold) AS sold FROM branch, fact_sales WHERE fact_sales.branch_key=branch.branch_key GROUP BY branch.branch_name;")
            ->queryAll();
        $it = $DB->createCommand("SELECT item.item_name, SUM(fact_sales.units_sold) AS sold FROM item, fact_sales WHERE item.item_key=fact_sales.item_key GROUP BY item.item_name;")
            ->queryAll();
        $prov = $DB->createCommand("SELECT location.state_or_province AS prov, SUM(fact_sales.units_sold) AS sold FROM location, fact_sales WHERE location.location_key=fact_sales.location_key GROUP BY location.state_or_province")
            ->queryAll();
        $quart = $DB->createCommand("SELECT time.quarter, SUM(fact_sales.units_sold) AS sold FROM time, fact_sales WHERE time.time_key=fact_sales.time_key GROUP BY time.quarter;")
            ->queryAll();
        return $this->render('index', compact("branch", "it", "prov", "quart"));
    }

    /**
     * Login action.
     *
     * @return Response|string
     */
    public function actionLogin()
    {
        if (!Yii::$app->user->isGuest) {
            return $this->goHome();
        }

        $model = new LoginForm();
        if ($model->load(Yii::$app->request->post()) && $model->login()) {
            return $this->goBack();
        }

        $model->password = '';
        return $this->render('login', [
            'model' => $model,
        ]);
    }

    /**
     * Logout action.
     *
     * @return Response
     */
    public function actionLogout()
    {
        Yii::$app->user->logout();

        return $this->goHome();
    }

    /**
     * Displays contact page.
     *
     * @return Response|string
     */
    public function actionContact()
    {
        $model = new ContactForm();
        if ($model->load(Yii::$app->request->post()) && $model->contact(Yii::$app->params['adminEmail'])) {
            Yii::$app->session->setFlash('contactFormSubmitted');

            return $this->refresh();
        }
        return $this->render('contact', [
            'model' => $model,
        ]);
    }

    /**
     * Displays about page.
     *
     * @return string
     */
    public function actionAbout()
    {
        return $this->render('about');
    }
}
